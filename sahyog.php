                        <center><b>“SAHYOG: SAHYOG HMARA SANGHARSH AAPKA”</b></center>
<p>When someone aims to be a civil servant, apart from questions involving the difficulty level of the Union and State Public Service Commission exam and the other processes, the expenses and time involved in preparing for it unavoidably crop up. Ironically, private players have catered to this domain for long but when young Civil Services Officers come forward to meet these requirement of aspirants then even underprivileged and students of remote areas can crack these prestigious exam. Sub Division administration Rampur in the leadership of then SDM Rampur decided to initiate 'Sahyog' program for aspiring candidates preparing to qualify the UPSC, HPPSC , CDS, NDA and all other competitive exams. Main idea behind this ambitious program was to demolish the general notion that civil services are for the elite class alone.</p>
<p><b>“SAHYOG: SAHYOG HMARA SANGHARSH AAPKA”</b> Initiative was started by <b><i>SDM Rampur Sh. Surender Mohan ( HAS 2014 batch)</i></b> for aspirants of Sub-Division Rampur and adjoining Sub Division with aim to provide free mentorship and guidance for various competitive examination like Exam conducted by UPSC ( IAS/CDS/NDA/AC-CPF), HPPSC (HAS/ NT/ Allied Services) and SSC. This initiative initiated with various components which include:</p><br>
•	To strengthen existing library in Rampur Town with various competitive examination books and magzines.<br>
•	To conduct carrer counselling sessions by IAS/ HAS Officers for aspirants twice in a month.<br>
•	Weekly current affairs handouts for aspirants.<br>
•	Live doubt sessions by experts of sponsoring institute.<br>
•	Mock test series for Prelims and Mains examination of UPSC/PCS.<br>
<p>Initially, initiative commenced in Nov. 2020 and mentorship and guidance sessions was conducted by SDM Rampur and more than 150 aspirants participated overwhelmingly in these sessions. Many students, who hail from this region, end up wasting their time due to lack of guidance and clarity. Many aspirants fail to clear the examination as they do not have specific books to read from. In order to solve this problem, efforts was made by SDM Rampur to facilitate aspirants with quality reference books, magzines and journals. Through fund raising campaign on social media and with the help of various other stake holders an amount of Rs. One lakh was collected and spent on adding more than seven hundred competitive examination books, monthly magzines and journals (subscribed for one/ two years).</p>
<p>Thereafter, other officers of District administration were associated in this program for mentoring and guiding the students. Deputy Commissioner Shimla Sh. Aditya Negi ( IAS 2013 batch) in Feb. 2021 interacted with the aspirants and encouraged them to pursue their dream. The free coaching facility provided great support to the poor and deprived students whose guidance was taken care of by sub divisional administration Rampur. This initiative was unique itself as it involve provision of direct counselling by IAS and PCS officers for the aspirants. The officers shared their experiences, strategies and insights about the UPSC exam preparation particularly and other examinations generally which will help the students develop a better understanding of the exam.</p>
<p>Further various lectures and complete study material covering the entire syllabus of various examinations was made available online through various social media platform( Whatsapp & Telegram) and live doubt solving sessions to help the candidates were conducted by engaging experts from sponsoring partner.</p>
<p>The guidance provided by the officers helped them with quick preparation and techniques to qualify IAS/HAS Prelims and write good quality answers in the mains examination of IAS/HAS. In a bid to help civil services aspirants through this initiative , various IAS/HAS officer came forward to share their notes, strategies and guidance on social media platforms.</p>
<p>Covid situation had not only affected aspirants education but their morale, too. In such a scenario, this initiative at sub divisional level by SDM Rampur was really helpful and blessing for aspirants aspring to crack various competitive examinations.</p>
<p>This initiative was in its full swing and flourished in first half of Year 2021 for bringing changes into life of many aspirants as many of them cleared preliminary examination of PCS and other exams. But after transfer of then SDM, mentorship and guidance sessions were conducted through webinar and continuous interaction with aspirants community is being made through social media platforms.</p> 
<p>In today’s scenario Government jobs are a big dream for the underprivileged but they can neither afford the time nor the cost of coaching for several years, So this type of initiative at Sub Divisional level are proving great help for all sections of society. When IAS, IPS officers come and speak to aspirants directly, it automatically boost their confidence. Apart from the confidence aspect, students also get a glance into the actual cosmos of the officers they are interacting with or being guided by — one that cannot be acquired by their good news tales about how they made it.</p>
<p>As Mr. Surender Mohan,H.A.S has joined back as SDM Rampur, so this ambitious program is again in full swing and various sessions are being organized for motivation and inspiring aspirants. The domain of this program is in increasing mode as focus is also being made on Class 9th to 12th students by covering various school by officers to guide students regarding their career counseling.</p>
